/**
 * Express server - REST API and static frontend
 * Environment: PORT, MONGODB_URI, JWT_SECRET, MAX_FILE_SIZE_MB
 */
const path = require('path');
require('dotenv').config({ path: path.join(__dirname, '.env') });
const express = require('express');
const cors = require('cors');
const connectDB = require('./config/db');
const authRoutes = require('./routes/auth');
const recordsRoutes = require('./routes/records');
const uploadRoutes = require('./routes/upload');
const communicationsRoutes = require('./routes/communications');
const REGIONS = require('./config/regions');

connectDB();

const app = express();
const PORT = process.env.PORT || 3000;

// CORS - allow frontend origin if different
app.use(cors());
app.use(express.json());
app.use(express.urlencoded({ extended: true }));

// Public: list of regions for login dropdown
app.get('/api/regions', (req, res) => res.json(REGIONS));

// API routes
app.use('/api/auth', authRoutes);
app.use('/api/upload', uploadRoutes);
app.use('/api/records/:recordId/communications', communicationsRoutes);
app.use('/api/records', recordsRoutes);

// Serve frontend static files (HTML, CSS, JS)
const frontendPath = path.join(__dirname, '..', 'frontend');
app.use(express.static(frontendPath));

// Global error handler for multer (file size, etc.)
app.use((err, req, res, next) => {
  if (err.code === 'LIMIT_FILE_SIZE') {
    return res.status(400).json({ error: 'File too large. Maximum size is ' + (process.env.MAX_FILE_SIZE_MB || '5') + 'MB.' });
  }
  if (err.message && err.message.includes('xlsx')) {
    return res.status(400).json({ error: err.message });
  }
  next(err);
});

app.listen(PORT, () => {
  console.log(`Server running at http://localhost:${PORT}`);
});
